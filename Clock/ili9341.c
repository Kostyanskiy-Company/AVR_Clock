void TFTDrawLine(uint16_t color, unsigned int x1, unsigned int y1, unsigned int x2, unsigned int y2)
{
	int steep = abs(y2-y1)>abs(x2-x1);
	if (steep)
	{
		swap(x1,y1);
		swap(x2,y2);
	}
	if(x1>x2)
	{
		swap(x1,x2);
		swap(y1,y2);
	}
	int dx,dy;
	dx=x2-x1;
	dy=abs(y2-y1);
	int err=dx/2;
	int ystep;
	if(y1<y2)	ystep = 1;
	else		ystep = -1;
	for (;x1<=x2;x1++)
	{
		if (steep)	TFTDrawPixel(y1,x1,color);
		else TFTDrawPixel(x1,y1,color);
		err-=dy;
		if (err<0)
		{
			y1 += ystep;
			err=dx;
		}
	}
}

void TFT_fillrect(uint16_t x,uint16_t y,uint16_t w,uint16_t h,uint16_t colour)
{
	if((x >=X_SIZE) || (y >=Y_SIZE)) return;
	if((x+w-1)>=X_SIZE)
	w=X_SIZE-x;
	if((y+h-1)>=Y_SIZE)
	h=Y_SIZE-y;

	LCD_SetCursorPosition(x, y, x+w-1, y+h-1);

	for(y=h; y>0; y--)
	{
		for(x=w; x>0; x--)
		{
			TFT9341_SendData(colour>>8);
			TFT9341_SendData(colour);
		}
	}
}

void DrawChar(int x, int y, unsigned char c, uint16_t color, uint16_t bg, int size)
{
	
	if((x >=X_SIZE) || (y >=Y_SIZE) || ((x + 6 * size - 1) < 0) || ((y + 8 * size - 1) < 0)) return;
	
	for (int8_t i=0; i<6; i++ ) {
		
		uint8_t line;
		
		if (i == 5) line = 0x0; else line = pgm_read_byte(font+(c*5)+i);
		
		for (int8_t j = 0; j<8; j++) {
			if (line & 0x1) {
				if (size == 1) // default size
				TFTDrawPixel(x+i, y+j, color);
				else {  // big size
					TFT_fillrect(x+(i*size), y+(j*size), size, size, color);
				}
				} else if (bg != color) {
				if (size == 1) // default size
				TFTDrawPixel(x+i, y+j, bg);
				else
				{  // big size
					TFT_fillrect(x+i*size, y+j*size, size, size, bg);
				}
			}
			line >>= 1;
		}
	}
}

void TFTDrawString(unsigned char *str, int x, int y, uint16_t color, uint16_t bg, int size)
{
    char i = 0;
	while (str[i])
	{
	
		DrawChar(x + (i * 6 * size), y, str[i], color, bg, size);		
		++i;
	}
}

