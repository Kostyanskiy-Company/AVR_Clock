void  SPI_MasterInit ( void )
{
   
   DDRB |=  (1<<LCD_SCK)|(1<<LCD_MISO)|(1<<LCD_MOSI)|(1<<LCD_DC)|(1<<LCD_CS);
   PORTB &=~((1<<LCD_DC)|(1<<LCD_CS));
   DDRB |=(1<<LCD_RESET);
   SPCR = (1<<SPE)|(1<<MSTR)|(0<<CPHA)|(1<<CPOL);
   //SPSR |=(1<<SPI2X);
}

int SPI_MasterTransmit(uint8_t cData){
  
  SPDR = cData;
  
  while (! (SPSR & ( 1 << SPIF)));
  return  SPDR; 
}
void SPI_SlaveInit(void)
{
  DDRB = (1<<LCD_MISO);
  SPCR = (1<<SPE);
}

char SPI_SlaveReceive(void)
{
  while(!(SPSR & (1<<SPIF)));
  return SPDR;
}
void TFT9341_SendCommand(unsigned char data)
{
  DC_LOW;
  CS_LOW;
  SPI_MasterTransmit(data);
  CS_HIGH;
}

void TFT9341_SendData(unsigned char data)
{
  DC_HIGH;
  CS_LOW;
  SPI_MasterTransmit(data);
  CS_HIGH;
}
void TFT9341_SetRotation(unsigned char r)
{
	TFT9341_SendCommand(0x36);
	switch(r)
	{
		case 0:
		TFT9341_SendData(0x48);
		X_SIZE = 240;
		Y_SIZE = 320;
		break;
		case 1:
		TFT9341_SendData(0x28);
		X_SIZE = 320;
		Y_SIZE = 240;
		break;
		case 2:
		TFT9341_SendData(0x88);
		X_SIZE = 240;
		Y_SIZE = 320;
		break;
		case 3:
		TFT9341_SendData(0xE8);
		X_SIZE = 320;
		Y_SIZE = 240;
		break;
	}
}

